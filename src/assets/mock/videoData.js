// 获取分类视频
export function getVideosByCategory(category) {
  return allVideos.filter(video => video.category === category);
}

// 获取最近添加的视频
export function getRecentVideos(limit = 20) {
  return [...allVideos]
    .sort((a, b) => new Date(b.uploadTime) - new Date(a.uploadTime))
    .slice(0, limit);
}